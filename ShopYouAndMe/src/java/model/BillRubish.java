package model;

/**
 *
 * @author Thanh
 */
public class BillRubish {
    User user;
    Cart cart;
    String payment;
    String address;
    String phone;

    public BillRubish() {
    }

    public BillRubish(User user, Cart cart, String payment, String address, String phone) {
        this.user = user;
        this.cart = cart;
        this.payment = payment;
        this.address = address;
        this.phone = phone;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Cart getCart() {
        return cart;
    }

    public void setCart(Cart cart) {
        this.cart = cart;
    }

    public String getPayment() {
        return payment;
    }

    public void setPayment(String payment) {
        this.payment = payment;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }


    
}
